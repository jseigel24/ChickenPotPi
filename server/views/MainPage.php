ChickenPotPi is a project being created as part of the International Space Apps Challenge (<a hres="http://spaceappschallenge.org/"> spaceappschallenge.org</a> ).
The goal for this project is to create a Raspberry Pi based system for backyard poultry management. With many of the end-users of this program located in rural areas, Internet access may be limited. To address this, the team’s goal is to create a web server on a Raspberry Pi, then build an application with PHP and MySQL.
This application will be capable of tracking the growth and development of the user’s flock (number or birds, eggs laid, fertilized eggs collected, health issue, etc.) as well as the budgeting (sales, feed expenses, other purchases) required by small poultry farmers.
