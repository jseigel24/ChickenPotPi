<strong>
4. What do I need to raise my baby chick?
</strong>
<br />
The first 60 days are very important in a bay chick, the most important think you have to keep in mind are:
<br />
<strong>
Housing
</strong>
<br />
The chick’s first house is called brooder. The brooder can be anything from a simple cardboard box to a fancy brooder. There are many options in the markets. The important thing is to keep the floor clean and dry. The size of the brooders depends on the number of chick; they should have enough room to move around.
<br />
<strong>
Temperature
</strong>
<br />
Temperature should be 90-100 degrees for the first week. Yu should decrease the temperature by 5 degrees each week until the chicks have their feathers (5-8 weeks old). You can heat the brooder with a 100 watt-bulb or with an actual heat lamp. Having a thermometer is a good idea but don’t forget that the best way to check the temperature is by looking how the chicks behave.
<br />
<strong>
Water
</strong>
<br />
Baby chicks drink a lot of water, and they must always have fresh water. Clean the water container at least once a day.
If Chicks food is called “crumbles”, you can choose by use medicated food nor not. If you choose not medicated food make sure that the brooder is clean all the time in order to reduce the risk of potential diseases.
<br />
<strong>
Play time
</strong>
<br />
Baby chicks are very curious, when possible play with them so they get use to human contact.
